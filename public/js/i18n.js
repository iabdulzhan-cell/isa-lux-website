let currentLang = localStorage.getItem('lang') || 'ru';

export function t(key) { return translations[currentLang]?.[key] || translations.ru[key] || key; }
export function getLang() { return currentLang; }
export function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;
  applyTranslations();
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
}
export function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (el.tagName === 'INPUT' && el.placeholder !== undefined) el.placeholder = val;
    else el.innerHTML = val;
  });
}
export function initI18n() {
  document.documentElement.lang = currentLang;
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
  applyTranslations();
}

window.i18n = { t, getLang, setLang, initI18n, applyTranslations };
export default { t, getLang, setLang, initI18n };
